    MCI_SetOpenLoopVoltage(&Mci[M1]);

    // set duty cycle
    OL_UpdateVoltage(&OpenLoop_Params, ((DUTY_CYCLE * 32767) / 100));

    // execute speed ramp
    MCI_ExecSpeedRamp(&Mci[M1], 60, DURATION_MS);

    // run motor for 5 seconds
    MC_StartMotor1();
    HAL_Delay(5000);

